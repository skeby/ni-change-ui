// Normalizes a world-atlas geography name to the office-location country name.
export const normalizeGeoName = (geoName: string): string => {
  if (!geoName) return geoName;
  const alias = GEO_NAME_ALIASES[geoName.toLowerCase()];
  return alias || geoName;
};
